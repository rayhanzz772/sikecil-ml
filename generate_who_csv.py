# GENERATE CSV
# ============================================================

def generate_csv(data_boys, data_girls, filepath):
    rows = []
    for row in data_boys:
        month, sd3neg, sd2neg, sd1neg, median, sd1pos, sd2pos, sd3pos = row
        L, M, S = estimate_lms(median, sd2neg, sd2pos)
        rows.append([month, "L", L, M, S])

    for row in data_girls:
        month, sd3neg, sd2neg, sd1neg, median, sd1pos, sd2pos, sd3pos = row
        L, M, S = estimate_lms(median, sd2neg, sd2pos)
        rows.append([month, "P", L, M, S])

    # Urutkan by sex, then age
    rows.sort(key=lambda r: (r[1], r[0]))

    with open(filepath, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["age", "sex", "L", "M", "S"])
        writer.writerows(rows)

    print(f"✅ Generated: {filepath} ({len(rows)} rows)")


