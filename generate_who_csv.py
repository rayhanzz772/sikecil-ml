"""
generate_who_csv.py
Konversi data WHO dari whoStandards.ts ke format CSV LMS
yang digunakan oleh who_service.py di backend.

Strategi LMS dari data SD:
    Untuk distribusi normal, jika L=1 (Box-Cox linear):
    M = median
    S = (sd2pos - sd2neg) / (4 * M)  — estimasi koefisien variasi

    Khusus berat badan (distribusi skewed), L dihitung dari:
    (sd2pos - M) / (M - sd2neg)  -> jika rasio tidak 1, berarti skewed (L != 1)
    Kita estimasi L menggunakan metode least-squares dari tabel SD.
"""

