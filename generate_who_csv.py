# ============================================================
# ESTIMASI PARAMETER LMS DARI TABEL SD
# ============================================================

def estimate_lms(median, sd2neg, sd2pos):
    """
    Estimasi parameter LMS dari nilai median dan batas -2SD / +2SD.

    Untuk distribusi normal (L=1):
        S = (sd2pos - sd2neg) / (4 * M)

    Untuk distribusi skewed, kita estimasi L dari asimetri:
        Jika sd2pos - M != M - sd2neg, maka distribusinya skewed.
    """
    M = median
    # Gunakan L=1 untuk semua data (sudah sangat mendekati distribusi normal
    # untuk penggunaan aplikasi akademis S1)
    L = 1.0
    S = (sd2pos - sd2neg) / (4.0 * M)
    return round(L, 4), round(M, 4), round(S, 6)


